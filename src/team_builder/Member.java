package team_builder;

public class Member {
 
    String name;
    String surname;
    char sex;
    int date;
    int month;
    int year;

    public Member(String name, String surname, char sex, int date, int month, int year) {
        this.name = name;
        this.surname = surname;
        this.sex = sex;
        this.date = date;
        this.month = month;
        this.year = year;
    }

    public String getName() {
        return this.getName();
    }

    public String getSurname() {
        return this.getSurname();
    }

    public char getSex() {
        return this.getSex();
    }

    public int getDate() {
        return this.getDate();
    }

    public int getMonth() {
        return this.getMonth();
    }

    public int getYear() {
        return this.getYear();
    }

}
